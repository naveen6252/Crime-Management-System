        // TODO add your handling code here:
        String id=t1.getText();
        String inpass=p1.getText();
        String pass=null;
        try {
            Connection con = null;
            Class.forName("oracle.jdbc.driver.OracleDriver");
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe",authorization.username,authorization.password);
            PreparedStatement pst = con.prepareStatement("Select * from userdetails where username=?");
            pst.setString(1, id);
            ResultSet x = pst.executeQuery();
            while (x.next()) {
                pass = x.getString(4);
            }
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
         if (inpass.equals(pass)) {
                    this.dispose();
                    new CitizenPage().setVisible(true);
                }
