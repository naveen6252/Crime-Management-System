    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String username=un.getText();
        String fname="a";
        try{
            Connection con=null;
            Class.forName("oracle.jdbc.driver.OracleDriver");
            con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe",authorization.username,authorization.password);
            PreparedStatement pst=con.prepareStatement("select fname from userdetails where username=?");
            pst.setString(1, username);
            ResultSet x=pst.executeQuery();
            while(x.next()){
                fname=x.getString(1);
            }
            if(fname.equals("a")){
                JOptionPane.showMessageDialog(this, "Wrong Username!");
            }
            else if(!(pass1.getText().equals(pass2.getText()))){
                JOptionPane.showMessageDialog(this, "Password does not match");
            }
            else{
                PreparedStatement ps=con.prepareStatement("update userdetails set fname=?,lname=?,password=? where username=?");
                ps.setString(1, fn.getText());
                ps.setString(2, ln.getText());
                ps.setString(3, pass1.getText());
                ps.setString(4, username);
                int y=ps.executeUpdate();
                if(y>0){
                    JOptionPane.showMessageDialog(this, "Successfully Updated!");
                }
                con.close();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

